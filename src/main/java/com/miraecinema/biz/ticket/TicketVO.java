public class TicketVO {
	String tcode;
	String rnum;
	String mvstart;
	String seet;
	String tmvname;
	String ttime;
	String tid;

	public String getTcode() {
		return tcode;
	}

	public void setTcode(String tcode) {
		this.tcode = tcode;
	}

	public String getRnum() {
		return rnum;
	}

	public void setRnum(String rnum) {
		this.rnum = rnum;
	}

	public String getMvstart() {
		return mvstart;
	}

	public void setMvstart(String mvstart) {
		this.mvstart = mvstart;
	}

	public String getSeet() {
		return seet;
	}

	public void setSeet(String seet) {
		this.seet = seet;
	}

	public String getTmvname() {
		return tmvname;
	}

	public void setTmvname(String tmvname) {
		this.tmvname = tmvname;
	}

	public String getTtime() {
		return ttime;
	}

	public void setTtime(String ttime) {
		this.ttime = ttime;
	}

	public String getTid() {
		return tid;
	}

	public void setTid(String tid) {
		this.tid = tid;
	}
}
